#include "libft.h"

void	ft_putnbr(int n)
{
	long tmp;

	tmp = (long)n;
	if (tmp < 0)
	{
		tmp = -tmp;
		ft_putchar('-');
	}
	if (tmp >= 10)
		ft_putnbr(tmp / 10);
	ft_putchar(tmp % 10 + 48);
}
